export const buildAIInsight = (symbol: string): AIInsight => {
  const stock = MOCK_STOCKS.find((s) => s.symbol === symbol.toUpperCase()) ?? MOCK_STOCKS[0];
  const bullish = stock.changePercent > 0;
  return {
    symbol: stock.symbol,
    companyName: stock.name,
    summary: `${stock.name} is a ${bullish ? "bullish" : "cautious"} position in the ${stock.sector} sector. Momentum is ${bullish ? "positive with strong volume support" : "consolidating after a recent pullback"}, and fundamentals remain ${stock.peRatio < 30 ? "attractively valued" : "priced for growth"}.`,
    businessOverview: stock.description ?? "",
    growthPotential: `Revenue growth is expected to compound at 12-18% annually driven by ${stock.industry.toLowerCase()} tailwinds. Margin expansion opportunities remain in the medium term.`,
    financialHealth: `Balance sheet is ${stock.marketCap > 500_000_000_000 ? "fortress-grade" : "healthy"} with strong free cash flow generation. Debt/equity ratios stay well within sector norms.`,
    fundamentalAnalysis: `P/E of ${stock.peRatio} vs sector median ~25. EPS of ${stock.eps} is trending upward. Dividend yield of ${stock.dividendYield}% provides a modest income cushion.`,
    technicalAnalysis: `Price is trading ${stock.price > stock.previousClose ? "above" : "below"} short-term moving averages. RSI is neutral (~55). MACD ${bullish ? "shows a bullish crossover" : "is flattening near the signal line"}.`,
    pros: [
      "Category-leading market position",
      "Strong operating margins and pricing power",
      "Robust R&D pipeline",
      "Institutional accumulation over the past quarter",
    ],
    cons: [
      "Elevated valuation multiples",
      "Sensitivity to macro rate cycles",
      "Competitive pressure from new entrants",
    ],
    riskScore: Math.round(30 + Math.abs(stock.beta * 25)),
    confidenceScore: bullish ? 76 : 58,
    recommendation: bullish
      ? stock.changePercent > 2
        ? "Strong Buy"
        : "Buy"
      : stock.changePercent < -2
        ? "Hold"
        : "Hold",
    targetPrice: Number((stock.price * (bullish ? 1.18 : 1.06)).toFixed(2)),
    futureOutlook: `12-month outlook is ${bullish ? "constructive" : "neutral"}. Watch upcoming earnings and sector rotation dynamics as key catalysts.`,
  };
};
