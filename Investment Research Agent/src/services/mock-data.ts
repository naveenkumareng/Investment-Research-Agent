export const generateChartData = (symbol: string, range: TimeRange): PricePoint[] => {
  const stock = MOCK_STOCKS.find((s) => s.symbol === symbol) ?? MOCK_STOCKS[0];
  const points =
    range === "1D"
      ? 78
      : range === "1W"
        ? 35
        : range === "1M"
          ? 30
          : range === "6M"
            ? 180
            : range === "1Y"
              ? 252
              : range === "5Y"
                ? 260
                : 500;
  const seed = symbol.split("").reduce((a, c) => a + c.charCodeAt(0), 0);
  const rand = rnd(seed);
  const volatility = range === "1D" ? 0.004 : range === "1W" ? 0.012 : 0.02;
  let price = stock.price * (range === "1D" ? 0.995 : range === "5Y" ? 0.45 : 0.85);
  const data: PricePoint[] = [];
  const now = Date.now();
  const stepMs =
    range === "1D"
      ? 5 * 60_000
      : range === "1W"
        ? 4 * 60 * 60_000
        : range === "1M"
          ? 24 * 60 * 60_000
          : range === "6M"
            ? 24 * 60 * 60_000
            : range === "1Y"
              ? 24 * 60 * 60_000
              : range === "5Y"
                ? 7 * 24 * 60 * 60_000
                : 14 * 24 * 60 * 60_000;
  for (let i = points; i >= 0; i--) {
    price = price * (1 + (rand() - 0.48) * volatility);
    data.push({
      time: new Date(now - i * stepMs).toISOString(),
      price: Number(price.toFixed(2)),
      volume: Math.round(1_000_000 + rand() * 5_000_000),
    });
  }
  // Anchor last point to current
  data[data.length - 1].price = stock.price;
  return data;
};

